# Addition

print(5 + 4)

# Subtraction

print(9 - 4)

# Multiplication

print(9 * 5)

# Division

print(9 / 2)

# Modular operation; "%" is the modulus operator

print(9 % 5)

# Exponentiation; "**" is the exponent operator

print(2 ** 3)

# Order for operations follows standard mathematical conventions;
# parentheses have the highest precedence, followed by exponentiation,
# multiplication and division, and finally addition and subtraction. Always remember "PEMDAS"

print(2 + (3 * 4) - 5 ** 2)